function generateUniqueMeta(slug, ctx) {
  const vars = buildVars(slug, ctx);
  // Разделяем пул шаблонов на "без city" и "с city", чтобы geo-страницы
  // никогда не пересекались с нишевыми.
  const hasCity = Boolean(vars.city);
  const pool = hasCity
    ? META_TEMPLATES.filter((t) => t.includes('{city') || t.includes('{h1}'))
    : META_TEMPLATES.filter((t) => !t.includes('{city'));
  // Salt: nicheId + cityId — чтобы разные ниши И разные города одной ниши
  // не пересекались на одном индексе шаблона.
  const nicheSalt = ctx?.nicheId || (slug.match(/^[a-z-]+/) || ['x'])[0];
  const citySalt = ctx?.city?.id || '';
  const template = pool[hashNum(slug, `meta:${nicheSalt}:${citySalt}`) % pool.length];
  let meta = fill(template, vars);
  // Два хвоста из двух пулов по 96 фраз: 96 × 96 = 9216 комбинаций
  // → уникальность на 2 800 страниц гарантирована.
  const h1 = crypto.createHash('sha256').update(`meta-tail:${slug}`).digest();
  const tail1 = META_TAIL_PHRASES[h1.readUInt16BE(0) % META_TAIL_PHRASES.length];
  const h2 = crypto.createHash('sha256').update(`meta-tail2:${slug}:${citySalt}`).digest();
  const tail2 = META_TAIL_PHRASES2[h2.readUInt16BE(0) % META_TAIL_PHRASES2.length];
  meta = /[.!?]\s*$/.test(meta) ? `${meta} ${tail1} ${tail2}` : `${meta}. ${tail1} ${tail2}`;
  return meta;
}

