function flattenSectionBody(section) {
  const parts = [];
  if (section.body) parts.push(section.body);
  if (section.paragraphs) parts.push(...section.paragraphs);
  if (section.h3Blocks) {
    section.h3Blocks.forEach((b) => {
      if (b.paragraphs) parts.push(...b.paragraphs);
    });
  }
  return parts.join('\n\n');
}

function buildComparePage(slug) {
  const cfg = PAGE_CONFIG[slug];
  if (!cfg) return null;

  const comp = COMPETITORS[cfg.competitor];
  const intro = cfg.mode === 'versus' ? buildVersusIntro(comp) : buildAlternativeIntro(comp);
  const sections = buildCoreSections(comp, cfg.mode).map((s) => ({
    ...s,
    body: flattenSectionBody(s),
  }));

  const faq = buildFaq(comp, cfg.mode);
  const metaDescription = cfg.mode === 'versus'
    ? `${cfg.h1}. Подробное сравнение функций, тарифов, CRM, Telegram и MAX. Честный разбор для салонов и мастеров.`
    : `Ищете альтернативу ${comp.name}? ${SITE_NAME} — онлайн-запись и CRM с бесплатным стартом. Подробный обзор и план перехода.`;

  const page = {
    slug,
    page_type: 'compare',
    cluster: cfg.mode === 'versus' ? 'crm' : 'booking',
    niche: comp.id,
    title: `${cfg.h1} | ${SITE_NAME}`,
    meta_description: metaDescription,
    h1: cfg.h1,
    intro,
    sections,
    faq,
    related_slugs: [
      'online-zapis-dlya-klientov',
      'crm-dlya-klientov',
      cfg.mode === 'versus' ? `alternativa-${comp.id}` : `woner-vs-${comp.id}`,
      'crm-dlya-salona-krasoty',
      'resheniya',
    ].filter((s, i, arr) => arr.indexOf(s) === i && s !== slug),
    priority: cfg.priority,
    published: true,
    toc: sections.map((s, i) => ({ id: `section-${i}`, label: s.h2 })),
  };

  page.word_count = countPageWords(page);
  return page;
}

