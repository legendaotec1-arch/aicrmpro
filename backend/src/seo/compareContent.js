function wordCount(text) {
  return String(text || '').split(/\s+/).filter(Boolean).length;
}

function countPageWords(page) {
  let total = wordCount(page.intro);
  for (const s of page.sections || []) {
    total += wordCount(s.h2) + wordCount(s.body);
    if (s.paragraphs) s.paragraphs.forEach((p) => { total += wordCount(p); });
    if (s.h3Blocks) {
      s.h3Blocks.forEach((b) => {
        total += wordCount(b.h3);
        (b.paragraphs || []).forEach((p) => { total += wordCount(p); });
      });
    }
    if (s.bullets) s.bullets.forEach((b) => { total += wordCount(b); });
    if (s.table?.rows) s.table.rows.flat().forEach((c) => { total += wordCount(c); });
  }
  for (const f of page.faq || []) total += wordCount(f.q) + wordCount(f.a);
  return total;
}

