async function applyFix(client, fix) {
  const r = await client.query(
    `SELECT faq, related_slugs FROM seo_pages WHERE slug = $1`,
    [fix.slug]
  );
  if (!r.rows.length) {
    console.log(`[skip] ${fix.slug} not found`);
    return false;
  }
  const row = r.rows[0];
  const faq = typeof row.faq === 'string' ? JSON.parse(row.faq) : row.faq;
  const rel = row.related_slugs || [];

  const existingQ = new Set((faq || []).map((f) => f.q));
  const mergedFaq = [...(faq || [])];
  for (const f of (fix.extraFaq || [])) {
    if (!existingQ.has(f.q)) mergedFaq.push(f);
  }

  const existingRel = new Set(rel);
  const mergedRel = [...rel];
  for (const s of (fix.extraRelated || [])) {
    if (!existingRel.has(s)) mergedRel.push(s);
  }

  await client.query(
    `UPDATE seo_pages SET intro = $2, faq = $3::jsonb, related_slugs = $4, updated_at = NOW() WHERE slug = $1`,
    [fix.slug, fix.intro, JSON.stringify(mergedFaq), mergedRel]
  );
  return true;
}

async function markProtected(client) {
  // Ставим флаг manuallyEdited на все 10 страниц, чтобы seed не затёр
  await client.query(
    `UPDATE seo_pages SET extras = COALESCE(extras, '{}'::jsonb) || '{"manuallyEdited": true}'::jsonb
     WHERE slug = ANY($1::text[])`,
    [FIXES.map(f => f.slug)]
  );
}

async function main() {
  const client = await db.connect();
  let updated = 0;
  let skipped = 0;
  try {
    await client.query('BEGIN');
    for (const fix of FIXES) {
      const ok = await applyFix(client, fix);
      if (ok) {
        updated += 1;
        console.log(`[ok] ${fix.slug} (intro → ${fix.intro.length} chars)`);
      } else {
        skipped += 1;
      }
    }
    await markProtected(client);
    await client.query('COMMIT');
  } catch (err) {
    await client.query('ROLLBACK');
    throw err;
  } finally {
    client.release();
  }
  console.log(`\nDONE. updated=${updated}, skipped=${skipped}, total=${FIXES.length}`);
  await db.end();
  process.exit(0);
}

main().catch((err) => {
  console.error('FATAL', err);
  process.exit(1);
});
