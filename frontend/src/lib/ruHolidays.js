function pad(n) {
  return String(n).padStart(2, '0');
}

function nameForKey(dateKey) {
  const mmdd = dateKey.slice(5);
  return NAMED[mmdd] || 'Государственный праздник';
}

function genericYear(year) {
  const out = new Map();
  for (let d = 1; d <= 8; d++) {
    const key = `${year}-01-${pad(d)}`;
    out.set(key, nameForKey(key));
  }
  for (const [mmdd, name] of Object.entries(NAMED)) {
    if (mmdd.startsWith('01-')) continue;
    const key = `${year}-${mmdd}`;
    out.set(key, name);
  }
  return out;
}

/** @returns {Map<string, string>} dateKey -> название праздника */
export function getRuHolidays(year) {
  const extra = YEAR_EXTRA[year];
  if (extra) {
    const map = new Map();
    for (const key of extra) {
      map.set(key, nameForKey(key));
    }
    return map;
  }
  return genericYear(year);
}

export function formatDateKey(date) {
  const d = date instanceof Date ? date : new Date(date);
  return `${d.getFullYear()}-${pad(d.getMonth() + 1)}-${pad(d.getDate())}`;
}

export function eachDateInRange(from, to) {
  const start = new Date(from);
  const end = new Date(to);
  start.setHours(12, 0, 0, 0);
  end.setHours(12, 0, 0, 0);
  if (start > end) return eachDateInRange(to, from);
  const keys = [];
  const cur = new Date(start);
  while (cur <= end) {
    keys.push(formatDateKey(cur));
    cur.setDate(cur.getDate() + 1);
  }
  return keys;
}
